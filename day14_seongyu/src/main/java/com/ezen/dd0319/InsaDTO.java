package com.ezen.dd0319;

public class InsaDTO {
	
	String id, revel;
	int family, money;
	
	public InsaDTO() {		}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRevel() {
		return revel;
	}
	public void setRevel(String revel) {
		this.revel = revel;
	}
	public int getFamily() {
		return family;
	}
	public void setFamily(int family) {
		this.family = family;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	
}
